//condicionales
//if, if else, switch

let numero1 = 10;
let numero2 = 20;

if(numero1 > numero2){
  console.log("es mayor")
}else{
  console.log("es menor")
}

//switch
let dia = "martes";
switch (dia){
    case "lunes":
      console.log("es lunes");
      break;
    case "martes":
      console.log("es martes");
      break;
    default:
      console.log("no es lunes ni martes");
}
  